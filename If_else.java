public class If_else 
{
	public static void main(String[] args)
	{
		int a=2;
		if (a>0)
		{
			System.out.println("Given number is Greater then 0");
		}
		else if(a<0)
		{
			System.out.println("Given number is Smaller then 0");
		}
		else
		{
			System.out.println("Given number is equal to 0");
		}
	}
}