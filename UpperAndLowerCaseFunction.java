
public class UpperAndLowerCaseFunction 
{
	public static void main(String[] args)
	{
		String str="This is the program of java";
		String str1=str.toUpperCase(); 		//this function is used to print all letter in Upper Case
		System.out.println(str);
		System.out.println(str1);
		
		String strNew="LETTER ARE IN CAPITAL LETTER";
		String str2=strNew.toLowerCase();		//this function is used to print all letter in Lower Case
		System.out.println(strNew);
		System.out.println(str2);
		
	}
}
