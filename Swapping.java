public class Swapping 
{

	public static void main(String[] args) 
	{
		int a=10,b=20,c;
		System.out.println("current valaue a= " +a + " and b=  "+ b);
		c=a;
		a=b;
		b=c;
		System.out.println("reverse valaue a= " +a + " and b=  "+ b);
		
		System.out.println(" ************************** ");
		
		int A=10 , B=20 ;
		System.out.println("current valaue A= " +A + " and B=  "+ B);
		A=A+B;
		B=A-B;
		A=A-B;
		System.out.println("reverse valaue A= " +A + " and B=  "+ B);
		
	}

}