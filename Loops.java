public class Loops 
{

	public static void main(String[] args) 
	{
		System.out.println("Loop concept");
		
		//while Loop 
		
		int i=1;
		while(i>0)
		{
			System.out.println("Hello number is grater then 0")	;	
			i--;
		}	
		
		//for loop
		
		for(int j=0;j<10;j++)
		{
			System.out.println("My name is kritika " + (j+1) + "Time");
		}
		
		//do while
		
		int k=1;
		do
		{
			System.out.println(k+ "\t");
			k++;
		}while(k<=10);
			
	}

}